CREATE DATABASE PETBOT;

CREATE TABLE TUTOR
(
    CODDISCORD VARCHAR(200) PRIMARY KEY,
    NOME VARCHAR(80),
    USUARIO VARCHAR(20),
    SENHA VARCHAR(50),
    CPF VARCHAR(20),
    DATANASCIMENTO DATE,
    TELEFONE VARCHAR(20),
    ENDERECO VARCHAR(300),
    TUTOR_ID INT IDENTITY
);

CREATE TABLE PET
(
    CODPET INT PRIMARY KEY IDENTITY,
    NOME VARCHAR(50),
    ESPECIE VARCHAR(70),
    RACA VARCHAR(70),
    COR VARCHAR(50),
    SEXO VARCHAR(1),
    PORTE VARCHAR(9),
    IDADE INT,
    PESO FLOAT,
    OBSEVACAO VARCHAR(500),
    FK_TUTOR_ID VARCHAR(100) FOREIGN KEY REFERENCES TUTOR(TUTOR_ID)
);

CREATE TABLE CONSULTA
(
    CODCONSULTA INT PRIMARY KEY IDENTITY,
    HORA VARCHAR(5),
    DIA DATE,
    DESCRICAO VARCHAR(500),
    FK_CODPET INT FOREIGN KEY REFERENCES PET(CODPET)
);